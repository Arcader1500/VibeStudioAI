// Ambient engine
// ---------------------------------------------------------------------------

function generateAmbientEngine(): string {
  return `
let _ambientNodes = []

export function startAmbient() {
  const ctx  = getCtx()
  stopAmbient()

  // Deep drone pad
  function makeDrone(freq, vol = 0.04) {
    const osc  = ctx.createOscillator()
    const gain = ctx.createGain()
    const flt  = ctx.createBiquadFilter()
    osc.type = 'sine'
    osc.frequency.value = freq
    flt.type = 'lowpass'; flt.frequency.value = 600
    gain.gain.value = vol
    osc.connect(flt); flt.connect(gain); gain.connect(ctx.destination)
    osc.start()
    _ambientNodes.push(osc, gain)
  }

  makeDrone(55,   0.05)   // sub bass
  makeDrone(110,  0.03)   // bass
  makeDrone(220,  0.02)   // lower mid
  makeDrone(330,  0.015)  // mid

  // Slow LFO modulation on the bass drone
  const lfo  = ctx.createOscillator()
  const lfoG = ctx.createGain()
  lfo.type = 'sine'
  lfo.frequency.value = 0.12   // 0.12 Hz = ~8s cycle
  lfoG.gain.value = 15         // modulate ±15 Hz
  lfo.connect(lfoG)
  // Connect LFO to first drone's frequency
  if (_ambientNodes[0]) lfoG.connect(_ambientNodes[0].frequency)
  lfo.start()
  _ambientNodes.push(lfo, lfoG)
}

export function stopAmbient() {
  _ambientNodes.forEach(n => { try { n.stop?.(); n.disconnect?.() } catch {} })
  _ambientNodes = []
}
`
}

