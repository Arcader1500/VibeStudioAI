function generateSfxEngine(style: MusicStyle): string {
  const baseWave = style === 'chiptune' ? 'square' : style === 'synthwave' ? 'sawtooth' : 'triangle'

  return `
export const sfx = {
  /** Shoot / fire bullet */
  shoot() {
    const ctx = getCtx()
    const osc = ctx.createOscillator()
    const g   = ctx.createGain()
    osc.type = 'square'
    osc.frequency.setValueAtTime(880, ctx.currentTime)
    osc.frequency.exponentialRampToValueAtTime(220, ctx.currentTime + 0.08)
    g.gain.setValueAtTime(0.15, ctx.currentTime)
    g.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + 0.1)
    osc.connect(g); g.connect(ctx.destination)
    osc.start(); osc.stop(ctx.currentTime + 0.12)
  },

  /** Enemy hit */
  hit() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    // Noise burst via white noise buffer
    const buf = ctx.createBuffer(1, ctx.sampleRate * 0.08, ctx.sampleRate)
    const data = buf.getChannelData(0)
    for (let i = 0; i < data.length; i++) data[i] = Math.random() * 2 - 1
    const src = ctx.createBufferSource()
    const flt = ctx.createBiquadFilter()
    const g   = ctx.createGain()
    src.buffer = buf
    flt.type = 'bandpass'; flt.frequency.value = 800; flt.Q.value = 0.5
    g.gain.setValueAtTime(0.3, t)
    g.gain.exponentialRampToValueAtTime(0.001, t + 0.08)
    src.connect(flt); flt.connect(g); g.connect(ctx.destination)
    src.start()
  },

  /** Player/entity death */
  death() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    for (let i = 0; i < 4; i++) {
      playTone(ctx, 440 - i * 80, '${baseWave}', t + i * 0.07, 0.1, 0.12)
    }
    // Noise tail
    const buf = ctx.createBuffer(1, ctx.sampleRate * 0.3, ctx.sampleRate)
    const d   = buf.getChannelData(0)
    for (let j = 0; j < d.length; j++) d[j] = (Math.random() * 2 - 1) * (1 - j / d.length)
    const src = ctx.createBufferSource()
    const g   = ctx.createGain()
    src.buffer = buf; g.gain.value = 0.2
    src.connect(g); g.connect(ctx.destination)
    src.start(t + 0.28)
  },

  /** Coin / item pickup */
  pickup() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    const notes = [523, 659, 784, 1047]
    notes.forEach((hz, i) => playTone(ctx, hz, 'sine', t + i * 0.06, 0.08, 0.15))
  },

  /** Wave start announcement */
  waveStart() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    const notes = [261, 329, 392, 523]
    notes.forEach((hz, i) => playTone(ctx, hz, 'square', t + i * 0.1, 0.15, 0.12))
  },

  /** Game over jingle */
  gameOver() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    const notes = [523, 494, 440, 392, 349, 330, 294, 261]
    notes.forEach((hz, i) => playTone(ctx, hz, '${baseWave}', t + i * 0.12, 0.18, 0.13))
  },

  /** Level complete / win */
  win() {
    const ctx = getCtx()
    const t   = ctx.currentTime
    const notes = [523, 587, 659, 784, 880, 1047]
    notes.forEach((hz, i) => playTone(ctx, hz, 'sine', t + i * 0.09, 0.15, 0.14))
  },

  /** Player invincibility flash */
  shield() {
    const ctx = getCtx()
    const osc = ctx.createOscillator()
    const g   = ctx.createGain()
    osc.type = 'sine'
    osc.frequency.setValueAtTime(1200, ctx.currentTime)
    osc.frequency.exponentialRampToValueAtTime(600, ctx.currentTime + 0.15)
    g.gain.setValueAtTime(0.12, ctx.currentTime)
    g.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + 0.18)
    osc.connect(g); g.connect(ctx.destination)
    osc.start(); osc.stop(ctx.currentTime + 0.2)
  },
}
`
}

